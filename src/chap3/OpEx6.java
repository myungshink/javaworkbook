package chap3;

public class OpEx6 {
	public static void main(String[] args) {
		System.out.println(10/8);  //1
		System.out.println(10/-8); //-1
		System.out.println(-10/8); //-1
		System.out.println(-10/-8);//1
		System.out.println(10%8);  //2
		System.out.println(10%-8); //2
		System.out.println(-10%8); //-2
		System.out.println(-10%-8);//-2
	}
}
